test_that("removeSameBin", {
  calOne <- matrix(0, 3, 7)
  rownames(calOne) <- c("peakOneL2", "sizeOneL2", "heightOneL2")
  calOne[1, ] <- c(11, 12, 12, 13, 14, 14, 15)
  calOne[2, ] <- c(100, 103.8, 104.2, 108, 111.8, 112.2, 116)
  calOne[3, ] <- c(200, 100, 1000, 100, 100, 1000, 100)
  calOneNew <- removeSameBin(calOne)

  expect_equal(ncol(calOneNew), 3)
  expect_equal(as.numeric(calOneNew[1, 1]), 11)
  expect_equal(as.numeric(calOneNew[1, 2]), 13)
  expect_equal(as.numeric(calOneNew[1, 3]), 15)
  expect_equal(as.numeric(calOneNew[2, 1]), 100)
  expect_equal(as.numeric(calOneNew[2, 2]), 108)
  expect_equal(as.numeric(calOneNew[2, 3]), 116)
  expect_equal(as.numeric(calOneNew[3, 1]), 200)
  expect_equal(as.numeric(calOneNew[3, 2]), 100)
  expect_equal(as.numeric(calOneNew[3, 3]), 100)
})
